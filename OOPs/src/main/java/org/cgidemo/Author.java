package org.cgidemo;

public class Author {
    private String authorName;
    private String authDOB;

    public String getAuthorName() {
        return authorName;
    }

    public void setAuthorName(String authorName) {
        this.authorName = authorName;
    }

    public String getAuthDOB() {
        return authDOB;
    }

    public void setAuthDOB(String authDOB) {
        this.authDOB = authDOB;
    }
}
